# Trabalhando com Variáveis

######################## Atribuindo o valor 1 à variável var_teste ########################
var_teste = 1
# Imprimindo o valor da variável
print(var_teste)

############################## Declaração Múltipla ###############################
pessoa1, pessoa2, pessoa3 = "Bob", "Maria", "Ana"
